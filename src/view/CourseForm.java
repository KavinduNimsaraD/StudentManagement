    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

try {
            Course course = new Course();
            course.setCourseName(txtCourseName.getText());
            course.setDuration(txtDuration.getText());

            CourseController controller = new CourseController();
            boolean isSaved = controller.addCourse(course);

            if (isSaved) {
                javax.swing.JOptionPane.showMessageDialog(this, "Course Saved Successfully!");
                loadAllCourses(); // Save වුණ ගමන් Table එක අලුත් වෙනවා
                
                txtCourseName.setText("");
                txtDuration.setText("");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to save course.");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }


        
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblCoursesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCoursesMouseClicked
        
        // 1. ක්ලික් කරපු පේළිය අඳුරගන්නවා
        int selectedRow = tblCourses.getSelectedRow();

        // 2. Table Model එක ගන්නවා
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) tblCourses.getModel();

        // 3. ID එක අරන් selectedCourseId එකට දාගන්නවා
        selectedCourseId = Integer.parseInt(dtm.getValueAt(selectedRow, 0).toString());

        // 4. Course Name සහ Duration එක Text Fields වලට දානවා
        txtCourseName.setText(dtm.getValueAt(selectedRow, 1).toString());
        txtDuration.setText(dtm.getValueAt(selectedRow, 2).toString());
        
        
    }//GEN-LAST:event_tblCoursesMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    
        try {
            if (selectedCourseId == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a course from the table first!");
                return;
            }

            Course course = new Course();
            course.setCourseId(selectedCourseId); // Update කරද්දී ID එක අනිවාර්යයි
            course.setCourseName(txtCourseName.getText());
            course.setDuration(txtDuration.getText());

            CourseController controller = new CourseController();
            boolean isUpdated = controller.updateCourse(course);

            if (isUpdated) {
                javax.swing.JOptionPane.showMessageDialog(this, "Course Updated Successfully!");
                loadAllCourses(); 
                
                txtCourseName.setText("");
                txtDuration.setText("");
                selectedCourseId = 0; 
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to update course.");
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
        
        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        
        try {
            if (selectedCourseId == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a course from the table first!");
                return;
            }

            int option = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this course?", "Delete Confirmation", javax.swing.JOptionPane.YES_NO_OPTION);
            
            if (option == javax.swing.JOptionPane.YES_OPTION) {
                CourseController controller = new CourseController();
                boolean isDeleted = controller.deleteCourse(selectedCourseId);

                if (isDeleted) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Course Deleted Successfully!");
                    loadAllCourses(); 
                    
                    txtCourseName.setText("");
                    txtDuration.setText("");
                    selectedCourseId = 0;
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Failed to delete course.");
                }
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

