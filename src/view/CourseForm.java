    private void loadAllCourses() {
     try {
         CourseController controller = new CourseController();
         ArrayList<Course> courseList = controller.getAllCourses();

         javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) tblCourses.getModel();
         dtm.setRowCount(0);

         for (Course course : courseList) {
             Object[] row = {course.getCourseId(), course.getCourseName(), course.getDuration()};
             dtm.addRow(row);
         }
     } catch (Exception e) {
         javax.swing.JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
     }
 }
    
    
    
    
    

