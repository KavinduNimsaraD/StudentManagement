    // Table එකට දත්ත Load කරන Method එක
    private void loadAllStudents() {
        try {
            StudentController controller = new StudentController();
            ArrayList<Student> studentList = controller.getAllStudents(); // Controller එකෙන් දත්ත ඉල්ලනවා
            
            // Table එකේ Model එක අරගන්නවා (මේකෙන් තමයි Table එකට දත්ත දාන්නේ)
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) tblStudents.getModel();
            dtm.setRowCount(0); // Table එකේ තියෙන පරණ දත්ත මකලා හිස් කරනවා
            
            // List එකේ ඉන්න එක එක ළමයාව අරන් Table එකේ පේළි (Rows) විදියට දානවා
            for (Student student : studentList) {
                Object[] row = {student.getStudentId(), student.getName(), student.getAge(), student.getContact()};
                dtm.addRow(row);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
