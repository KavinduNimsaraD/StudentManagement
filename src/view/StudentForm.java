    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    try {
            // 1. Text Fields වලින් දත්ත ටික ගන්නවා
            String name = txtName.getText();
            int age = Integer.parseInt(txtAge.getText()); // Age එක ඉලක්කමක් නිසා String එක int කරනවා
            String contact = txtContact.getText();

            // 2. ඒ දත්ත දාලා Student Object එකක් (අර අපි කතා කරපු පෙට්ටිය) හදනවා
            Student student = new Student();
            student.setName(name);
            student.setAge(age);
            student.setContact(contact);

            // 3. Controller එකට කතා කරලා Object එක යවනවා Save කරන්න
            StudentController controller = new StudentController();
            boolean isSaved = controller.addStudent(student);

            // 4. Save වුණාද නැද්ද කියලා බලලා පණිවිඩයක් (Message Box) පෙන්නනවා
            if (isSaved) {
                javax.swing.JOptionPane.showMessageDialog(this, "Student Saved Successfully!");
                
                loadAllStudents();
                // Save වුණාට පස්සේ Text Fields ටික ආයෙත් හිස් කරනවා
                txtName.setText("");
                txtAge.setText("");
                txtContact.setText("");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to save student.");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }    // TODO add your handling code here:
    
    
    
    
    
    
    
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblStudentsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentsMouseClicked
      
// 1. ක්ලික් කරපු පේළියේ (Row) අංකය ගන්නවා
        int selectedRow = tblStudents.getSelectedRow();

        // 2. Table එකේ Model එක ගන්නවා දත්ත එලියට ගන්න
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel) tblStudents.getModel();

        // 3. 0 වෙනි තීරුවේ (Column) තියෙන ID එක අරන් අර අපි උඩ හදපු variable එකට දාගන්නවා
        selectedStudentId = Integer.parseInt(dtm.getValueAt(selectedRow, 0).toString());

        // 4. අනිත් තීරු (1, 2, 3) වල තියෙන දත්ත අරන් Text Fields වලට දානවා
        txtName.setText(dtm.getValueAt(selectedRow, 1).toString());
        txtAge.setText(dtm.getValueAt(selectedRow, 2).toString());
        txtContact.setText(dtm.getValueAt(selectedRow, 3).toString());        
// TODO add your handling code here:
    }//GEN-LAST:event_tblStudentsMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

try {
            // 1. Table එකෙන් කෙනෙක්ව තෝරලා නැත්නම් Error එකක් දෙනවා
            if (selectedStudentId == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first!");
                return; // මෙතනින් කෝඩ් එක නවත්තනවා
            }

            // 2. අලුත් විස්තර ටික අරගෙන Student Object එකක් හදනවා
            Student student = new Student();
            student.setStudentId(selectedStudentId); // Update එකට ID එක අනිවාර්යයි!
            student.setName(txtName.getText());
            student.setAge(Integer.parseInt(txtAge.getText()));
            student.setContact(txtContact.getText());

            // 3. Controller එකට යවලා Update කරනවා
            StudentController controller = new StudentController();
            boolean isUpdated = controller.updateStudent(student);

            // 4. Update වුණා නම් Table එක අලුත් කරලා Text Fields හිස් කරනවා
            if (isUpdated) {
                javax.swing.JOptionPane.showMessageDialog(this, "Student Updated Successfully!");
                loadAllStudents(); // අලුත් දත්ත Table එකට Load වෙනවා
                
                txtName.setText("");
                txtAge.setText("");
                txtContact.setText("");
                selectedStudentId = 0; // වැඩේ ඉවර නිසා ID එක ආයේ 0 කරනවා
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Failed to update student.");
            }

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter a valid number for Age.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }


        
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            // 1. කෙනෙක්ව තෝරලා නැත්නම් Error එකක් දෙනවා
            if (selectedStudentId == 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Please select a student from the table first!");
                return;
            }

            // 2. මකන්න කලින් ඇත්තටම මකන්නද කියලා අහනවා (Software Engineering Standard)
            int option = javax.swing.JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Delete Confirmation", javax.swing.JOptionPane.YES_NO_OPTION);
            
            // 3. Yes එබුවොත් විතරක් මකනවා
            if (option == javax.swing.JOptionPane.YES_OPTION) {
                StudentController controller = new StudentController();
                boolean isDeleted = controller.deleteStudent(selectedStudentId); // ID එක විතරයි යවන්නේ

                if (isDeleted) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Student Deleted Successfully!");
                    loadAllStudents(); // Table එක අලුත් කරනවා
                    
                    txtName.setText("");
                    txtAge.setText("");
                    txtContact.setText("");
                    selectedStudentId = 0;
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Failed to delete student.");
                }
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
        
        
        
        
    }//GEN-LAST:event_btnDeleteActionPerformed

